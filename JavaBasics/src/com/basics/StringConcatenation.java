package com.basics;

public class StringConcatenation {

	public static void main(String[] args) {

		String name = "Naveen";
		String num = "1000";
		
		String x = "Hello";
		String y = "Selenium";
		
		int a = 100;
		int b = 200;
		
		System.out.println(a+b);// addition operator
		System.out.println(name+num);
		System.out.println(x+y);
		
	}

}
